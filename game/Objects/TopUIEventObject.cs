public class TopUIEventObject {
    public int coin;
    public int zs;
    public int tl;
    public int hp;
    public int time;
    public int weapon;
    public int power;

    public TopUIEventObject(int coin, int zs, int tl, int hp, int time, int weapon, int power)
    {
        this.coin = coin;
        this.zs = zs;
        this.tl = tl;
        this.hp = hp;
        this.time = time;
        this.weapon = weapon;
        this.power = power;
    }
}